async def detect_admin_command(message_text: str, user_id: int, is_admin: bool) -> Optional[Dict]:
    """
    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –∫–æ–º–∞–Ω–¥–æ–π-–¥–µ–π—Å—Ç–≤–∏–µ–º –æ—Ç –∞–¥–º–∏–Ω–∞
    
    Args:
        message_text: –¢–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è
        user_id: ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        is_admin: –Ø–≤–ª—è–µ—Ç—Å—è –ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∞–¥–º–∏–Ω–æ–º
    
    Returns:
        Dict —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –∫–æ–º–∞–Ω–¥–µ –∏–ª–∏ None
    """
    if not is_admin:
        return None
    
    if not message_text:
        return None
    
    text_lower = message_text.lower().strip()
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤ –∫–æ–º–∞–Ω–¥
    for command_name, command_info in AVAILABLE_COMMANDS.items():
        for keyword in command_info["keywords"]:
            if keyword in text_lower:
                # –ù–∞–π–¥–µ–Ω–∞ –∫–æ–º–∞–Ω–¥–∞, –∏–∑–≤–ª–µ–∫–∞–µ–º –ø–∞—Ä–∞–º–µ—Ç—Ä—ã
                return await extract_command_params(command_name, message_text, command_info)
    
    return None

