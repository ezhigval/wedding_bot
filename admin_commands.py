def extract_message_text_manual(message_text: str) -> str:
    """–†—É—á–Ω–æ–µ –∏–∑–≤–ª–µ—á–µ–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –∏–∑ –∫–æ–º–∞–Ω–¥—ã"""
    # –ò—â–µ–º —Ç–µ–∫—Å—Ç –≤ –∫–∞–≤—ã—á–∫–∞—Ö (–ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    quoted = re.search(r'["\'](.+?)["\']', message_text)
    if quoted:
        return quoted.group(1)
    
    # –ò—â–µ–º —Ç–µ–∫—Å—Ç –ø–æ—Å–ª–µ –¥–≤–æ–µ—Ç–æ—á–∏—è
    if ":" in message_text:
        parts = message_text.split(":", 1)
        if len(parts) > 1:
            text = parts[1].strip()
            # –£–±–∏—Ä–∞–µ–º –∫–∞–≤—ã—á–∫–∏ –µ—Å–ª–∏ –µ—Å—Ç—å
            text = text.strip('"').strip("'")
            return text
    
    # –ò—â–µ–º —Ç–µ–∫—Å—Ç –ø–æ—Å–ª–µ –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤
    patterns = [
        r'—Ä–∞—Å—Å—ã–ª–∫[–∞—É–∏].*?["\'](.+?)["\']',
        r'–æ—Ç–ø—Ä–∞–≤–∏—Ç—å.*?["\'](.+?)["\']',
        r'–Ω–∞–ø–∏—Å–∞—Ç—å.*?["\'](.+?)["\']',
        r'—Å–æ–æ–±—â–µ–Ω–∏–µ.*?["\'](.+?)["\']',
    ]
    
    for pattern in patterns:
        match = re.search(pattern, message_text, re.IGNORECASE)
        if match:
            return match.group(1)
    
    # –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Ç–µ–∫—Å—Ç –ø–æ—Å–ª–µ –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤
    text_lower = message_text.lower()
    keywords = ["—Ä–∞—Å—Å—ã–ª–∫–∞", "—Ä–∞—Å—Å—ã–ª–∫—É", "–æ—Ç–ø—Ä–∞–≤–∏—Ç—å", "–Ω–∞–ø–∏—Å–∞—Ç—å", "—Å–æ–æ–±—â–µ–Ω–∏–µ"]
    for keyword in keywords:
        if keyword in text_lower:
            idx = text_lower.find(keyword)
            text = message_text[idx + len(keyword):].strip()
            # –£–±–∏—Ä–∞–µ–º –¥–≤–æ–µ—Ç–æ—á–∏–µ –∏ –ø—Ä–æ–±–µ–ª—ã –≤ –Ω–∞—á–∞–ª–µ
            text = text.lstrip(": ").strip()
            # –£–±–∏—Ä–∞–µ–º –∫–∞–≤—ã—á–∫–∏ –µ—Å–ª–∏ –µ—Å—Ç—å
            text = text.strip('"').strip("'")
            if text:
                return text
    
    return message_text

def extract_rule_text_manual(message_text: str) -> str:
    """–†—É—á–Ω–æ–µ –∏–∑–≤–ª–µ—á–µ–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞ –ø—Ä–∞–≤–∏–ª–∞ –∏–∑ –∫–æ–º–∞–Ω–¥—ã"""
    # –ò—â–µ–º —Ç–µ–∫—Å—Ç –≤ –∫–∞–≤—ã—á–∫–∞—Ö (–ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    quoted = re.search(r'["\'](.+?)["\']', message_text)
    if quoted:
        return quoted.group(1)
    
